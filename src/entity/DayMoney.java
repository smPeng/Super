package entity;

public class DayMoney {
	//日期
	private String day;
	//当天营业额
	private double money;
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public double getMoney() {
		return money;
	}
	public void setMoney(double money) {
		this.money = money;
	}
}
